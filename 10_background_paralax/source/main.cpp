int main(void)
{
	videoSetMode(MODE_0_2D);
	vramSetBankA(VRAM_A_MAIN_BG);
	
	int bg0 = bgInit(0, BgType_Text8bpp, BgSize_T_256x256, 0, 1);
	int bg1 = bgInit(1, BgType_Text8bpp, BgSize_T_512x256, 1, 1);
	
	dmaCopy(palette, BG_PALETTE, sizeof(palette));
	dmaCopy(tiles, BG_BMP_RAM(1), sizeof(tiles));
	dmaCopy(map_back, BG_MAP_RAM(0), sizeof(map_back));
	dmaCopy(map0, BG_MAP_RAM(1), sizeof(map0));
	dmaCopy(map1, BG_MAP_RAM(2), sizeof(map1));
	dmaCopy(map2, BG_MAP_RAM(3), sizeof(map2));
	dmaCopy(map3, BG_MAP_RAM(4), sizeof(map3));

	int bg0x = 0;
	int bg1x = 0;
	int os = 1;
	while(true)
	{
		scanKeys();
		if (keysHeld() & KEY_LEFT)  {bg0x--; bg1x -= 4;}
		if (keysHeld() & KEY_RIGHT) {bg0x++; bg1x += 4;}

		if (bg1x < 0)
		{
			if (os == 1)
			{
				bg1x += 4;
				bg0x++;
			}
			else
			{
				bgSetMapBase(bg1, --os);
				bg1x = 255;
			}
		}

		if (bg1x > 255)
		{
			if (os == 3)
			{
				bg1x -= 4;
				bg0x--;
			}
			else
			{
				bgSetMapBase(bg1, ++os);
				bg1x = 0;
			}
		}

		bgSetScroll(bg0, bg0x, 0);
		bgSetScroll(bg1, bg1x, 0);
		bgUpdate();

		swiWaitForVBlank();
	}
	
	return 0;
}
